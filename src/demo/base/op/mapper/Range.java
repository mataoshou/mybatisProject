package demo.base.op.mapper;

public class Range {
	private int id;
	
	private String rangeName;
	
	public String getRangeName() {
		return rangeName;
	}

	public void setRangeName(String rangeName) {
		this.rangeName = rangeName;
	}

	private int type;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}


	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}
}
